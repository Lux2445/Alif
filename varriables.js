// Объявляю переменную для хранения текущего количества товаров на складе
var currentStock = 100;  

// Переменная для хранения количества поступивших товаров за день
let dailyIncomingStock = 50;  

// Константа для хранения максимальной емкости склада
const maxStockCapacity = 500;  

// Функция для добавления товаров на склад
function addStock(incoming) {
  dailyIncomingStock = incoming;  // Обновляем количество поступивших товаров
  console.log(`Поступило товаров за день: ${dailyIncomingStock}`);
  
  // Проверяю, не превысим ли мы емкость склада
  if (currentStock + dailyIncomingStock > maxStockCapacity) {
    console.log('Ошибка: превышена максимальная емкость склада!');
  } else {
    // Увеличиваю количество товаров на складе
    currentStock += dailyIncomingStock;  
    console.log(`Обновленное количество товаров на складе: ${currentStock}`);
  }
}

// Функция для продажи товаров
function sellStock(sold) {
  // Проверяю, достаточно ли товаров для продажи
  if (sold > currentStock) {
    console.log('Ошибка: недостаточно товаров на складе для продажи!');
  } else {
    // Уменьшаю количество товаров на складе
    currentStock -= sold;  
    console.log(`Продано товаров: ${sold}. Оставшееся количество товаров на складе: ${currentStock}`);
  }
}

// Вывожу текущее количество товаров и максимальную емкость склада
console.log(`Текущее количество товаров на складе: ${currentStock}`);
console.log(`Максимальная емкость склада: ${maxStockCapacity}`);

// Добавляю новые товары на склад
addStock(200);  

// Продаю товары
sellStock(50);

// Пробую добавить больше товаров, чем позволяет склад
addStock(300);  // Пытаюсь добавить 300 товаров, чтобы проверить ограничение
